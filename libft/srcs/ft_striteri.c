void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	x;

	x = 0;
	if (s[x] == '\0')
		return ;
	while (s[x] != '\0')
	{
		f(x, &s[x]);
		x++;
	}
}
